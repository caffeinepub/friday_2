import { IntentCategory } from './intentParser';

export type ResponseType = 'text' | 'code' | 'email' | 'search' | 'system' | 'weather' | 'reminder' | 'translation' | 'joke';

export interface GeneratedResponse {
  text: string;
  type: ResponseType;
  data?: Record<string, unknown>;
}

const jokes = [
  "Why do programmers prefer dark mode? Because light attracts bugs! 🐛",
  "Why did the AI go to therapy? It had too many deep learning issues! 🤖",
  "What do you call a computer that sings? A Dell! 🎵",
  "Why was the JavaScript developer sad? Because he didn't know how to 'null' his feelings! 😄",
  "How many programmers does it take to change a light bulb? None — that's a hardware problem! 💡",
];

const systemResponses: Record<string, string> = {
  open: "I'm initiating the launch sequence for that application. On a real system, I would use the OS process manager to open the requested application. The command would be executed with appropriate permissions.",
  volume: "Adjusting system volume. On a real system, I would interface with the audio subsystem to modify the master volume level.",
  screenshot: "Capturing screen. On a real system, I would use the display capture API to take a screenshot and save it to your Pictures folder.",
  brightness: "Adjusting display brightness. On a real system, I would interface with the display driver to modify backlight intensity.",
  shutdown: "Initiating shutdown sequence. On a real system, I would execute a graceful system shutdown after saving all open work.",
  default: "I've processed your system command. On a real system with full OS integration, I would execute this action directly. For now, I can describe exactly what steps would be taken.",
};

function extractCodeLanguage(input: string): string {
  const langs: Record<string, string> = {
    python: 'python', py: 'python', javascript: 'javascript', js: 'javascript',
    typescript: 'typescript', ts: 'typescript', html: 'html', css: 'css',
    java: 'java', cpp: 'cpp', 'c++': 'cpp', rust: 'rust', go: 'golang',
  };
  const lower = input.toLowerCase();
  for (const [key, val] of Object.entries(langs)) {
    if (lower.includes(key)) return val;
  }
  return 'javascript';
}

